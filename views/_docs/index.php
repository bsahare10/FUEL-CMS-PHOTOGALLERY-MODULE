<h2>Explore Pictures</h2>
<p>The Google Keywords sub module allows you to search a given domain with certain keywords to find out how highly you rank in the top 100.
Configuration options include:</p>
<ul>
	<li><strong>keyword_search_default_domain</strong> - the default domain to search for when using keywords.</li>
	<li><strong>keyword_google_num_results</strong> - the number of results google will return. Default is 100.</li>
	<li><strong>keyword_google_default_keywords</strong> - the default keywords to search for.</li>
	<li><strong>keyword_google_search_centerpoint</strong> - the centerpoint of the search used by google. <kbd>NOT CURRENTLY USED</kbd></li>
</ul>


